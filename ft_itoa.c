#include "libft.h"

char	*ft_itoa(int n)
{
	char	temp[12];
	int		len;

	if (!n)
		return (ft_strdup("0\0"));
	temp[11] = '\0';
	len = 0;
	while (n)
	{
		len++;
		temp[11 - len] = ((n < 0) * -1 * (n % 10) + (n > 0) * (n % 10)) + 48;
		if (n < 0 && !(n / 10))
			temp[10 - len++] = '-';
		n /= 10;
	}
	return (ft_strdup(temp + 11 - len));
}
